function addBurger() {
    let id = document.getElementById("code").value;
    let name = document.getElementById("name").value;
    let price = document.getElementById("price").value;
    let discount = document.getElementById("discount").value;
    let itemImage = document.getElementById("image").files[0];
    let image = itemImage ? URL.createObjectURL(itemImage) : "";
    let category = document.getElementById("select").value;

    if (id === "" || name === "" || price === "" || discount === "" || image === "") {
        alert("All fields are required!");
        return;
    } if (category == "Burgers") {
        menu.burgers.push({ id, name, price, discount, image });
    } else if (category == "Submarines") {
        menu.submarines.push({ id, name, price, discount, image });
    } else if (category == "Fries") {
        menu.fries.push({ id, name, price, discount, image });
    }
    else if (category == "Pasta") {
        menu.pasta.push({ id, name, price, discount, image });
    }
    else if (category == "Chicken") {
        menu.chicken.push({ id, name, price, discount, image });
    }
    else if (category == "Beverages") {
        menu.beverages.push({ id, name, price, discount, image });
    }

    loadItemTable();

}

function loadItemTable() {
    const burgerItemList = document.getElementById("table-body");
    burgerItemList.innerHTML = "";

    Object.keys(menu).forEach(category => {
        menu[category].forEach((item, index) => {
            const row = document.createElement("tr");

            row.innerHTML = `<th scope="row">${item.id}</th>
              <td>${item.name}</td>
              <td>${item.price}</td>
               <td>${item.discount}</td>
               <td><img src="${item.image}" alt="image" height="100" width="100"></td>
              <td>
              <button class="btn-edit" onclick="editItems('${category}', ${index})">Edit</button>
              <button class="btn-delete" onclick="deleteItems('${category}', ${index})">Delete</button>
            </td>`;

            burgerItemList.appendChild(row);
        });
    });

}

function editItems(category, index) {
    const item = menu[category][index];

    document.getElementById("code").value = item.id;
    document.getElementById("name").value = item.name;
    document.getElementById("price").value = item.price;
    document.getElementById("image").value = "";
    document.getElementById("discount").value = item.discount;

    menu[category].splice(index, 1);
    loadItemTable();

}

function deleteItems(category, index) {
    menu[category].splice(index, 1);
    loadItemTable();

}
